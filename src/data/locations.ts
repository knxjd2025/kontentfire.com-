// Helper functions
export function getStateBySlug(slug: string): State | undefined {
  return states[slug];
}

export function getCityBySlug(citySlug: string): City | undefined {
  return cities[citySlug];
}

export function getCitiesForState(stateSlug: string): City[] {
  return Object.values(cities).filter(city => city.stateSlug === stateSlug);
}

export function getAllStates(): State[] {
  return Object.values(states);
}

export function getAllCities(): City[] {
  return Object.values(cities);
}

export function getStateForCity(city: City): State | undefined {
  return states[city.stateSlug];
}

export function formatNumber(num: number): string {
  if (num >= 1000000) {
    return (num / 1000000).toFixed(1) + 'M';
  }
  if (num >= 1000) {
    return (num / 1000).toFixed(0) + 'K';
  }
  return num.toLocaleString();
}

export function formatPercent(decimal: number): string {
  return Math.round(decimal * 100) + '%';
}
